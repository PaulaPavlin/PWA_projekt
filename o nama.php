                                <em>„Trkalci“</em> trenutno broje 6 članova te su svi članovi reprezentacije Hrvatske u orijentacijskom trčanju (stručni stožer te juniorska reprezentacija). 
                                Zajedno treniramo, svojim savjetima podupiremo i podržavamo jedni druge te smo jako motivirani kako bi se što bolje pripremili za iduću natjecateljsku sezonu.
                                <br><br>Naš <b>CILJ</b> je popularizirati sport orijentacijsko trčanje te potaknuti što više ljudi da nas prati i podržava na putu do ostvarenja svojih ciljeva. Ne krijemo da su nam ciljevi veliki ;), 
                                no naš put do njih većinom je trnovit. Bez obzira na bilokakve poteškoće, nikad nismo niti ne želimo odustati. Nemamo sponzore niti donatore pa čak i odlaske na natjecanja na kojima nastupamo za reprezentaciju Hrvatske financiramo sami.
                                <br><br>Ukoliko se želite informirati o orijentacijskom trčanju i/ili podržati naš rad, preporučamo da posjetite stranicu <a href="../kontakt/kontakt.html">Kontakt</a>.
